        			<p><b>GotToVote!</b> is an example of how open data can be useful to ordinary citizens.</p>
        			
        			<p><b>GotToVote!</b> was built at virtually zero cost as a <a href="http://code4kenya.org">Code4Kenya</a> data journalism project. It demonstrates how data-driven tools can help media audiences act on the news they read / watch, by finding out how a national event such as the elections affects their personal lives or local communities.</p>
        			
        			<p><a href="http://code4kenya.org">Code4Kenya</a> is a pilot programme funded by the <a href="http://africanmediainitiative.org">Africa Media Initiative</a> through the Digital Projects Division. It embedded four Data Fellows into major Kenyan newsrooms and a civil society organisation, for five months, to help kickstart experimentation with data-driven civic engagement tools. The Data Fellows were supported by an external software development team. The program is implemented by the <a href="http://openinstitute.com">Open Institute</a>.</p>
        			
        			<p><a href="http://code4kenya.org">Code4Kenya</a> has inspired similar initiatives in Ghana and South Africa. Its success has prompted AMI to pledge additional funding to allow Code4Kenya's jAccelerator lab to continue building civic tech projects until the end of 2013.</p>
        			
        			<p>All the data used on <b>GotToVote!</b> is freely available on another Code4Kenya project: the <a href="http://africaopendata.org">AfricaOpenData</a> portal, which is the continent's largest repository of public data, ranging from government budget and tender information, to data about parliamentarians and other public officials. The original data was extracted from a pdf provided by the <a href="http://iebc.or.ke">Independent Electoral and Boundaries Commission (IEBC)</a>.</p>
        			
